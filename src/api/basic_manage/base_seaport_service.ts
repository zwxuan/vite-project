export const saveBaseSeaport = async (data: BaseSeaportItemProps, onUploadProgress?: (progress: number) => void): Promise<BaseSeaportItemProps> => {
  // 模拟上传进度
  if (onUploadProgress) {
    let progress = 0;
    const interval = setInterval(() => {
      progress += 10;
      onUploadProgress(progress);
      if (progress >= 100) {
        clearInterval(interval);
      }
    }, 1000);
  }
  return data;
}


